namespace RogueLibTerminal.Input
{
    public enum MouseButton
    {
		/// <summary>
        /// Unknown mouse button or no mouse buttons are down
        /// </summary>
        Unknown,
		/// <summary>
        /// Left Mouse Button is down
        /// </summary>
        Left,
		/// <summary>
        /// Right Mouse Button
        /// </summary>
        Right,
		/// <summary>
        /// Middle Mouse Button
        /// </summary>
        Middle,
		/// <summary>
        /// Extra Mouse Button #1
        /// </summary>
        X1,
		/// <summary>
        /// Extra Mouse Button #2
        /// </summary>
        X2
    }
}